package settersAndGettersNotes;

public class Student {
	
	/**
	 * The instance variable name of the student
	 */
	private String name;

	/**
	 * Get method that returns the name of the student
	 * @return the name
	 */
	public String getName() {
		return name;
	}

	/**
	 * Method that sets the name of the student
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}
	
}
