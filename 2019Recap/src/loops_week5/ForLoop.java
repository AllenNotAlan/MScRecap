public class ForLoop {

	public static void main(String[] args) {
		
		
		//variable declared
		int loop;
		// this is a for loop. Structure:
		//for(initialise; termination ;increment){};
		for(loop = 1; loop<10; loop++) {
			System.out.println("Hello World");
		}
		
		
	}

}
